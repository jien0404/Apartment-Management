class ResidentItem {
  final String name;
  final String dob;
  final String idNumber;
  final String age;
  final String status;
  final String room;
  final String phoneNumber;

  const ResidentItem({
    required this.name,
    required this.dob,
    required this.idNumber,
    required this.age,
    required this.status,
    required this.room,
    required this.phoneNumber,
  });
}